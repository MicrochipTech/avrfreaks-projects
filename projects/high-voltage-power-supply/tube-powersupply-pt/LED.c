/****************************************************************************
 * Include files
 ****************************************************************************/
 #include "config.h"
 #include "LED.h"

/****************************************************************************
 *
 * Function LED_Initialize()
 *
 * Initialize the hardware
 *
 ****************************************************************************/
 void LED_Initialize()
 {
    LED_On();    
 }


/****************************************************************************
 *
 * Function LED_Check
 *
 * Check status of LED, returns 1 if ON otherwise 0
 *
 ****************************************************************************/
 int LED_Check()
 {
    return ( 1 << LED_PIN == (LED_OUT & (1 << LED_PIN) ));
 }


/****************************************************************************
 *
 * Function LED_On
 *
 * Switch LED on
 *
 ****************************************************************************/
 void LED_On()
 {
    LED_OUT |= 1<< LED_PIN;
    LED_DDR |= 1<< LED_PIN;    
 }


/****************************************************************************
 *
 * Function LED_Off
 *
 * Switch LED off
 *
 ****************************************************************************/
 void LED_Off()
 {
    LED_OUT &= ~(1<< LED_PIN);    
 }


/****************************************************************************
 *
 * Function toggleLED()
 *
 * Switch on/off the LED
 *
 ****************************************************************************/
 void LED_Toggle()
 {
    if ( LED_Check() )
	{
	   LED_Off();
	}
	else
	{
	   LED_On();
	}    
 }
