void GLCD_English(uint8_t Ecode)		/* display a 8x16 Dot English(ASCII) */
{ 
  unsigned char i, signal;

  GLCD_xy_row(xcharacter,ycharacter,0);		// display upper row
  
  if(ycharacter <= 7)				// if y <= 7, CS1
    signal = 0x40;
  else						// if y >= 8, CS2
    signal = 0x80;
  for(i = 0; i <= 7; i++)
    GLCD_data(signal, pgm_read_byte(&E_font[Ecode][i]));

  GLCD_xy_row(xcharacter, ycharacter, 1);		// display lower row
  
  if(ycharacter <= 7)				// if y <= 7, CS1
    signal = 0x40;
  else						// if y >= 8, CS2
    signal = 0x80;
  for(i = 8; i <= 15; i++)
    { if((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == ycursor))
        GLCD_data(signal,pgm_read_byte(&E_font[Ecode][i]) | 0x80);
      else
        GLCD_data(signal,pgm_read_byte(&E_font[Ecode][i]));
    }

  ycharacter++;                                 // go next character position
  if(ycharacter == 16)
    { ycharacter = 0;
      xcharacter++;
    }
}

void GLCD_Korean(uint16_t Kcode)		/* display a 16x16 Dot Korean */
{ unsigned char i, signal;
  unsigned char cho_5bit, joong_5bit, jong_5bit;
  unsigned char cho_bul, joong_bul, jong_bul=0, jong_flag;
  unsigned int character;
  unsigned char Korean_buffer[32];		// 32 byte Korean font buffer

  if(ycharacter == 15)				// if can not display, go next line
    GLCD_English(0x20);

						// get cho, joong, jong 5 bit
  cho_5bit = pgm_read_byte(&table_cho[(Kcode >> 10) & 0x001F]);
  joong_5bit = pgm_read_byte(&table_joong[(Kcode >> 5) & 0x001F]);
  jong_5bit = pgm_read_byte(&table_jong[Kcode & 0x001F]);

  if(jong_5bit == 0)				// don't have jongsung 
    { jong_flag = 0;
      cho_bul = pgm_read_byte(&bul_cho1[joong_5bit]);
      if((cho_5bit == 1) || (cho_5bit == 16)) joong_bul = 0;
      else                                    joong_bul = 1;
    }
  else						// have jongsung
    { jong_flag = 1;
      cho_bul = pgm_read_byte(&bul_cho2[joong_5bit]);
      if((cho_5bit == 1) || (cho_5bit == 16)) joong_bul = 2;
      else                                    joong_bul = 3;
      jong_bul = pgm_read_byte(&bul_jong[joong_5bit]);
    }

  character = cho_bul*20 + cho_5bit;		// copy chosung font
  for(i = 0; i <= 31; i++)
    Korean_buffer[i] = pgm_read_byte(&K_font[character][i]);

  character = 8*20 + joong_bul*22 + joong_5bit;	// OR joongsung font
  for(i = 0; i <= 31; i++)
    Korean_buffer[i] |= pgm_read_byte(&K_font[character][i]);

  if(jong_flag == 1)				// if jongsung, OR jongsung font
    { character = 8*20 + 4*22 +jong_bul*28 + jong_5bit;
      for(i = 0; i <= 31; i++)
        Korean_buffer[i] |= pgm_read_byte(&K_font[character][i]);
    }

  GLCD_xy_row(xcharacter,ycharacter,0);		// display upper left row
  if(ycharacter <= 7)				// if y <= 7, CS1
    signal = 0x40;
  else						// if y >= 8, CS2
    signal = 0x80;
  for(i = 0; i <= 7; i++)
    GLCD_data(signal,Korean_buffer[i]);

  GLCD_xy_row(xcharacter,ycharacter,1);		// display lower left row
  if(ycharacter <= 7)				// if y <= 7, CS1
    signal = 0x40;
  else						// if y >= 8, CS2
    signal = 0x80;
  for(i = 16; i <= 23; i++)
    { if((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == ycursor))
         GLCD_data(signal,Korean_buffer[i] | 0x80);
      else
         GLCD_data(signal,Korean_buffer[i]);
    }

  ycharacter++;

  GLCD_xy_row(xcharacter,ycharacter,0);		// display upper right row
  if(ycharacter <= 7)				// if y <= 7, CS1
    signal = 0x40;
  else						// if y >= 8, CS2
    signal = 0x80;
  for(i = 8; i <= 15; i++)
    GLCD_data(signal,Korean_buffer[i]);

  GLCD_xy_row(xcharacter,ycharacter,1);		// display lower right row
  if(ycharacter <= 7)				// if y <= 7, CS1
    signal = 0x40;
  else						// if y >= 8, CS2
    signal = 0x80;
  for(i = 24; i <= 31; i++)
    { if((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == (ycursor+1)))
         GLCD_data(signal,Korean_buffer[i] | 0x80);
      else
         GLCD_data(signal,Korean_buffer[i]);
    }

  ycharacter++;                                 // go next character position
  if(ycharacter == 16)
    { ycharacter = 0;
      xcharacter++;
    }
}


void GLCD_character(uint8_t character)		/* display a character */
{ uint8_t i, signal, font_data;

  GLCD_xy(xcharacter,ycharacter);
  if(ycharacter <= 9)				// if y <= 9, CS1
    signal = 0x40;
  else						// if y >= 10, CS2
    signal = 0x80;
  for(i = 0; i <= 4; i++)
    //{ font_data = pgm_read_byte(&font[character-0x20][i]);
      { font_data = pgm_read_byte(&font[character-0x20][i]);
      if((cursor_flag == 1) && (xcharacter == xcursor) && (ycharacter == ycursor))
          GLCD_data(signal,font_data | 0x80);
      else
          GLCD_data(signal,font_data);
    }
  GLCD_data(signal,0x00);			// last byte 0x00

  ycharacter++;                                 // go next character position
  if(ycharacter == 20)
    { ycharacter = 0;
      xcharacter++;
    }
}

