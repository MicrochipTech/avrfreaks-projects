// acc_step is the size of the step thru the lookup table... 
volatile unsigned short acc_step = 0x4000;//15,625k 	//0x28F5;//10k 		//0x51EB;//20k 

// for conversion of portA to signed int...
int8_t port_in =0; 

// the accumulator carries the fractional precision in the lower 8 bits (P)...
// the higher bits are used to address the lookup table (L)...
// top bits are not reqrd, they are masked to roll-over the accumulator (x)
// xxxx xxLL LLLL LLLL PPPP PPPP  <-- accumulator structure
typedef union 
	{ 
  	unsigned long all; 
  	struct
  		{
  		uint8_t low; 
  		unsigned short hi; 
  		};
	} Union24; 

// .all is complete variable...  (xxxx xxLL LLLL LLLL PPPP PPPP)
// .hi is address to lookup table... (L)
// .low is fractional precision part... (P)
// new variable type is called Union24
Union24 accumulator; 



// interrupt to change frequency
// minumum step / resolution is 0.953674 Hz
// maximum step is -128 to +127 using 2's compliment for negative
//
// PINA = 0000 0001 = +1 = +0.953674 Hz
// PINA = 1111 1111 = -1 = -0.953674 Hz
//
// PINA = 0000 0011 = +3 = +(3 x 0.953674) Hz
// PINA = 1111 1101 = -3 = -(3 x 0.953674) Hz
//
// activated on INT0 PB6 
ISR (INT0_vect)
	{

	// read the port A and convert to int8_t (signed) port_in 
	port_in = PINA;

	// adjust the accumumlator step size
	acc_step += port_in; 

	// debounce a little for testing
	 _delay_ms(100);

	}

static inline void ioinit (void){

// PORTS AND PINS 
//
// Enable Port A for input
DDRA = 0x00;
// Enable internal pull ups for PORT A
PORTA=0xFF;
// PB2 & PB3 & PB4 as output // PB6 as input
DDRB = 0x1C;
//Enable internal pull ups for PB6 
PORTB=0x40;

// PWM SETUP
//
// set PWM inital value to 0
OCR1B = 0;
// Enable and lock PLL
PLLCSR |= _BV(PLLE);
// Wait for PLL to lock (app. 100 ms)
while(!(PLLCSR & _BV(PLOCK))){;}
// Set PLL as asynchronous PWM clock source
PLLCSR |= _BV(PCKE);
// PWM mode = oc1b on match
TCCR1A |= _BV(COM1B0);// COM1B0 = 1
// Set PWM Top value: OCR1C = 0xFF
OCR1C = 0xff;
// enable PWMB
TCCR1A |= _BV(PWM1B);
// TCCR1B |= _BV(CS12) | _BV(CS10);// CS13=0, CS12=1, CS11=0, CS10=1 => 4 Mhz
//TCCR1B |= _BV(CS11) | _BV(CS13) | _BV(CS13) | _BV(CS13); // all for max prescaler
TCCR1B |= _BV(CS10);//CS13=0, CS12=0, CS11=0, CS10=1 => 64 Mhz

// Enable int0 at PB6
GIMSK |= _BV(INT0);

// do not enable the OVF ISR vector...
// TIMSK |= _BV(TOIE1);

// enable interrupts
sei ();

} // end of ioinit


int main (void)
	{
	
	// initialize ports and pins and pwm...
    ioinit ();
 
    // run for loop forever...
	for(;;)
		{

/*



*/

		// soft interrupt for timer1 overflow...
		// takes fewer cycles than hard interrupt	
		// must take less than 64 cycles to excute before next OVF
		//
		// wait for OVF1 flag to indicate next pwm cycle
		// will trigger at base frequency 64MHz / 256 = 250kHz
		while (TIFR & (1<<TOV1))
			{

			// write a 1 to TOV1 to reset soft interrupt
			TIFR |= (1<<TOV1);	


			// Provide a zero crossing detection...
			// output on PB4, not exactly perfect timing
			// but very close...
			//
			// make pin PB4 low when OCR1B > 1/2 wave
			if (OCR1B < 127){PORTB &= ~_BV(PB4);}
			// make pin PB4 hi when OCR1B < 1/2 wave
			if (OCR1B > 127){PORTB |= _BV(PB4);}
			
		
			// add next step 
			accumulator.all += acc_step;
		
			// mask the top (0x03 <<8) 
			// keeps the address to the lookup table under 1023
			accumulator.hi &= 0x3FF;  
			
			



			// load the table data into PWM duty cycle register
			OCR1B=pgm_read_byte(&sinewave[accumulator.hi]); 					
	



			} // end of while (TIFR & (1<<TOV1))

		} // end of for(;;)
  
	}// end of main
