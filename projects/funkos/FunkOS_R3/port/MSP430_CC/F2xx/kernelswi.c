#include "types.h"
#include <msp430.h>

//---------------------------------------------------------------------------
void KernelSWI_Config(void)
{
  // Set P1IFG to true
  P1DIR &= ~0x01;
}

//---------------------------------------------------------------------------
void KernelSWI_Start(void)
{
    P1IE |= 0x01;
}

//---------------------------------------------------------------------------
void KernelSWI_Stop(void)
{
    P1IE &= ~0x01;
}

//---------------------------------------------------------------------------
void KernelSWI_Clear(void)
{
    P1IFG &= ~0x01;
}

//---------------------------------------------------------------------------
void KernelSWI_Trigger(void)
{
    P1IFG |= 0x01;
}
