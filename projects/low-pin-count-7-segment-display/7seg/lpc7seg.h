void sevenseg_init(); 		// initializes relevant I/O registers
void sevenseg_refresh(); 	// displays data. call at least every 10 ms
void sevenseg_update_fbuf();// updates frame buffer from character buffer
///////////////////////////////////////////////////////////////////////////////
#define S7N			12		//number of digits
#define S7SEGS		8		//number of segments.
#define S7_DELAY_US	5		//default brightness
/////////////////////////////////////////////////////////////////////////////// 
#define S7PORT			PORTB		// address output port
#define S7_MASK			0xff		// address output mask
/////////////////////////////////////////////////////////////////////////////// 
#define S7LATCH_PORT 	B
#define S7LATCH_PIN		4
#define S7GLOW_PORT 	B
#define S7GLOW_PIN		5
/////////////////////////////////////////////////////////////////////////////// 
extern uint8_t s7fbuf[S7N];		//frame buffer
extern uint8_t s7cbuf[S7N];		//character buffer
extern uint8_t s7brightness;			//in us
#endif
