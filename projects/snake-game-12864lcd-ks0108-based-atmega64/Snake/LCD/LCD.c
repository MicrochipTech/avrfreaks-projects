/*************************************************/
/*    填充一个像素数据  x,y为坐标 val为0或1      */
/*************************************************/
void OnePixel(uchar x,uchar y,uchar val)
{
	uchar *p = &Dis_Buf[ ((y>>3)<<7) + x ];//找到对应字节
	if(val)*p |= (1<<(y%8));		//修改对应位
	else *p &= ~(1<<(y%8));
}

unsigned char ReadPixel(uchar x,uchar y)
{
	uchar *p = &Dis_Buf[ ((y>>3)<<7) + x ];//找到对应字节
	if(*p & (1<<(y%8)))return 1;
	else return 0;
}



